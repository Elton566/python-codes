#comparison operators
a=10
print(a<5)

print(a>5)

print(a<=5)

print(a>=5)

print(a==5)

#logical operators
a=5
b=10
print(a==5 and b==10)

print(a==10 and b==5)

print(a==5 or b==6)

print(a==6 or b==7)

print(not a==5)
print(not a==8)

#membership operators
x=(1,2,3)
print(1 in x)
print(10 in x)

print(3 not in x)
